package Operators;

public class AssignmentOperator {
    public static void main(String[] args) {
        // Assignment Operators: =, +=, -=, *=, /=, %= to assign values.
        int a = 5;
        a += 5; // a + 5 = 10
        a -= 3; // a - 3 = 7 
        a *= 3; // a * 3 = 21
        a /= 3; // a / 3 = 7
        a %= 4; // a % 4 = 3 (Remainder)
        System.out.println(a);
    }
}
