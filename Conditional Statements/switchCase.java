public class switchCase {
    public static void main(String[] args) {
        int n = 4;

        switch (n) {
            case 1: System.out.println("Hello 1");
            break;
            case 2: System.out.println("Hello 2");
            break;
            case 3: System.out.println("Hello 3");
            break;
            default: System.out.println("Hello Default");
        }
    }
}
