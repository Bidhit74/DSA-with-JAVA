import java.util.ArrayList;
public class BuiltInPackages {
    public static void main(String[] args) {
        ArrayList<String> list = new ArrayList<>();
        list.add("Apple");
        list.add("Banana");
        
        System.out.println(list); // Output: [Apple, Banana]
    }
}

