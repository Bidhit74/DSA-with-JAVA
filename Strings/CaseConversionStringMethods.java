public class CaseConversionStringMethods {
    public static void main(String[] args) {
        String fullName = "Bidhit Kumar";

        //toUpperCase(): Converts all characters to uppercase.
        System.out.println("toUpperCase: " + fullName + " = " + fullName.toUpperCase());

        //toLowerCase(): Converts all characters to lowercase.
        System.out.println("toLowerCase: " + fullName + " = " + fullName.toLowerCase());
    }
}
